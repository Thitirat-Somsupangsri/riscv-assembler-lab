const InstrSpec *find_instruction(const char *mnemonic) {
    size_t i;

    if (mnemonic == NULL) {
        return NULL;
    }

    for (i = 0; i < sizeof(kInstructions) / sizeof(kInstructions[0]); ++i) {
        if (strcmp(kInstructions[i].name, mnemonic) == 0) {
            return &kInstructions[i];
        }
    }

    return NULL;
}
